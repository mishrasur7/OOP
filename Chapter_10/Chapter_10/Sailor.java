package Chapter_10;

public class Sailor {
	private String name;
	private String email;

	public Sailor(String name, String email) {
		this.name = name;
		this.email = email;
	}

	public String getName() {
		return this.name;
	}

	public void setEmail(String email) {
		this.email = email; 
	}
	public String getEmail() {
		return this.email;
	}
	
	
}
